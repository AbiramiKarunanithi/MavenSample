package com.abi.training.jenkins;

public class JenkinsCalc {
	
	//Method to ADD 2 numbers
	public int addNumbers(int numberOne, int numberTwo) {
		return numberOne + numberTwo;
	}
	
	//Method to SUBTRACT 2 numbers
	public int subtractNumbers(int numberOne, int numberTwo) {
		return numberOne - numberTwo;
	}

	//Method to Multiply 2 numbers
	public int multiplyNumbers(int numberOne, int numberTwo) {
		return numberOne * numberTwo;
	}
	
}
